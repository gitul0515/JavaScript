// 암묵적 타입 변환: 문자열로 변환 //
// 숫자 타입
console.log(0 + ''); // "0"
console.log(-0 + ''); // "0"
console.log(1 + ''); // "1"
console.log(-1 + ''); // "-1"
console.log(NaN + ''); // "NaN"
console.log(Infinity + ''); // "Infinity"
console.log(-Infinity + ''); // "-Infinity"

// 불리언 타입
console.log(true + ''); // "true"
console.log(false + ''); // "false"

// null 타입
console.log(null + ''); // "null"

// undefined 타입
console.log(undefined + ''); // "undefined"
